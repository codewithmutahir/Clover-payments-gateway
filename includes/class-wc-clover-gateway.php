/**
 * WooCommerce Clover payment gateway.
 */
class WC_Clover_Gateway extends WC_Payment_Gateway
{

	/**
	 * Constructor.
	 */
	public function __construct()
	{
		$this->id                 = 'clover_gateway';
		$this->method_title       = __('Clover Payments', 'clover-gateway');
		$this->method_description = __('Accept credit and debit card payments via Clover with full order item sync.', 'clover-gateway');
		$this->has_fields         = true;
		$this->supports           = array(
			'products',
			'refunds',
		);

		$this->init_form_fields();
		$this->init_settings();

		$this->enabled              = $this->get_option('enabled', 'no');
		$this->title                = $this->get_option('title', __('Credit / Debit Card', 'clover-gateway'));
		$this->description          = $this->get_option('description', __('Pay securely with your credit or debit card via Clover.', 'clover-gateway'));
		$this->merchant_id          = $this->get_option('merchant_id');
		$this->api_token            = $this->get_option('api_token');
		$this->public_key           = $this->get_option('public_key');
		$this->private_key          = $this->get_option('private_key');
		$this->test_mode            = 'yes' === $this->get_option('test_mode', 'yes');
		$this->default_tax_rate_id  = $this->get_option('default_tax_rate_id', '');

		add_action('woocommerce_update_options_payment_gateways_' . $this->id, array($this, 'process_admin_options'));
		add_action('wp_enqueue_scripts', array($this, 'enqueue_scripts'));

		// Display saved card details in WooCommerce admin order screen.
		add_action('woocommerce_admin_order_data_after_order_details', array($this, 'display_card_details_admin'), 10, 1);

		// Display card details in the order-totals table on the My Account / order-view page.
		add_filter('woocommerce_get_order_item_totals', array($this, 'add_card_details_to_order_totals'), 10, 3);

		// Display card details on PDF invoices generated by the WooCommerce PDF Invoices plugin.
		add_action('wpo_wcpdf_after_order_data', array($this, 'display_card_details_pdf'), 10, 2);
	}

